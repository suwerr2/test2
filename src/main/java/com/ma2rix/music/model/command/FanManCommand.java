package com.ma2rix.music.model.command;

import java.io.Serializable;

import org.joda.time.DateTime;

/**
 * 팬리스트에 사용되는 command 클래스
 */

public class FanManCommand implements Serializable{

	private static final long serialVersionUID = 1L;
	
	private int fanNo;
	private int fanMemberNo;
	private int musicMemberNo;
	private String musicClass;
	private int musicNo;
	private DateTime regDateTime;
	private int memberNo;
	private String memberUserid;
	private String memberName;
	private String memberNickName;
	private String memberImage;
	private int fanCnt;
	
	public FanManCommand(){
		super();
	}
	
	public FanManCommand(int menu, int fanNo, int fanMemberNo, int musicMemberNo,
			String musicClass, int musicNo, DateTime regDateTime, int memberNo,
			String memberUserid, String memberName, String memberNickName,
			String memberImage, int fanCnt) {
		
		super();
		
		this.fanNo = fanNo;
		this.fanMemberNo = fanMemberNo;
		this.musicMemberNo = musicMemberNo;
		this.musicClass = musicClass;
		this.musicNo = musicNo;
		this.regDateTime = regDateTime;
		this.memberNo = memberNo;
		this.memberUserid = memberUserid;
		this.memberName = memberName;
		this.memberNickName = memberNickName;
		this.memberImage = memberImage;
		this.fanCnt = fanCnt;		
	}

	
	public int getFanNo() {
		return fanNo;
	}

	public void setFanNo(int fanNo) {
		this.fanNo = fanNo;
	}

	public int getFanMemberNo() {
		return fanMemberNo;
	}

	public void setFanMemberNo(int fanMemberNo) {
		this.fanMemberNo = fanMemberNo;
	}

	public int getMusicMemberNo() {
		return musicMemberNo;
	}

	public void setMusicMemberNo(int musicMemberNo) {
		this.musicMemberNo = musicMemberNo;
	}

	public String getMusicClass() {
		return musicClass;
	}

	public void setMusicClass(String musicClass) {
		this.musicClass = musicClass;
	}

	public int getMusicNo() {
		return musicNo;
	}

	public void setMusicNo(int musicNo) {
		this.musicNo = musicNo;
	}

	public DateTime getRegDateTime() {
		return regDateTime;
	}

	public void setRegDateTime(DateTime regDateTime) {
		this.regDateTime = regDateTime;
	}

	public int getMemberNo() {
		return memberNo;
	}

	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}

	public String getMemberUserid() {
		return memberUserid;
	}

	public void setMemberUserid(String memberUserid) {
		this.memberUserid = memberUserid;
	}

	public String getMemberName() {
		return memberName;
	}

	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}

	public String getMemberNickName() {
		return memberNickName;
	}

	public void setMemberNickName(String memberNickName) {
		this.memberNickName = memberNickName;
	}

	public String getMemberImage() {
		return memberImage;
	}

	public void setMemberImage(String memberImage) {
		this.memberImage = memberImage;
	}

	public int getFanCnt() {
		return fanCnt;
	}

	public void setFanCnt(int fanCnt) {
		this.fanCnt = fanCnt;
	}

	
}
