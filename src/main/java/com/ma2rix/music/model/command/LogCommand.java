package com.ma2rix.music.model.command;

import java.io.Serializable;

import org.joda.time.DateTime;

public class LogCommand implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private int logNo;
	private String actMenu;
	private int memberNo;
	private String actResult;
	private String actIp;
	private String regDateTime;
	private String dateFrom;
	private String dateTo;
	private int searchMemberNo;

	public LogCommand() {}

	public LogCommand(int logNo, String actMenu, int memberNo,
			String actResult, String actIp, String regDateTime,
			String dateFrom, String dateTo, int searchMemberNo	) {
		super();
		this.logNo	 = 	logNo;
		this.actMenu = 	actMenu;
		this.memberNo = memberNo;
		this.actResult = 	actResult;
		this.actIp = 	actIp;
		this.regDateTime = regDateTime;
		this.dateFrom = dateFrom;
		this.dateTo = dateTo;
		this.searchMemberNo = searchMemberNo;
		
	}

	public int getLogNo() {
		return logNo;
	}

	public void setLogNo(int logNo) {
		this.logNo = logNo;
	}

	public String getActMenu() {
		return actMenu;
	}

	public void setActMenu(String actMenu) {
		this.actMenu = actMenu;
	}

	public int getMemberNo() {
		return memberNo;
	}

	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}

	public String getActResult() {
		return actResult;
	}

	public void setActResult(String actResult) {
		this.actResult = actResult;
	}

	public String getActIp() {
		return actIp;
	}

	public void setActIp(String actIp) {
		this.actIp = actIp;
	}

	public String getRegDateTime() {
		return regDateTime;
	}

	public void setRegDateTime(String regDateTime) {
		this.regDateTime = regDateTime;
	}

	public String getDateFrom() {
		return dateFrom;
	}

	public void setDateFrom(String dateFrom) {
		this.dateFrom = dateFrom;
	}

	public String getDateTo() {
		return dateTo;
	}

	public void setDateTo(String dateTo) {
		this.dateTo = dateTo;
	}

	public int getSearchMemberNo() {
		return searchMemberNo;
	}

	public void setSearchMemberNo(int searchMemberNo) {
		this.searchMemberNo = searchMemberNo;
	}


}
